// 彩度調整用の簡易シェーダー
export const SaturationShader = {
    uniforms: {
        'tDiffuse': { value: null },
        'saturation': { value: 1.5 } // 1.0が標準。数値を上げると彩度が上がります
    },
    vertexShader: `
        varying vec2 vUv;
        void main() {
            vUv = uv;
            gl_Position = projectionMatrix * modelViewMatrix * vec4(position, 1.0);
        }
    `,
    fragmentShader: `
        uniform sampler2D tDiffuse;
        uniform float saturation;
        varying vec2 vUv;
        void main() {
            vec4 color = texture2D(tDiffuse, vUv);
            float gray = dot(color.rgb, vec3(0.299, 0.587, 0.114));
            gl_FragColor = vec4(mix(vec3(gray), color.rgb, saturation), color.a);
        }
    `
};

// スクリーン座標から平面(z=0)上のワールド座標に変換する関数
export function screenToWorldPlane(screenX: number, screenY: number, camera: THREE.Camera, targetZ = 0): THREE.Vector3 {
    const mouse = new THREE.Vector2(
        (screenX / window.innerWidth) * 2 - 1,
        -(screenY / window.innerHeight) * 2 + 1
    );

    const raycaster = new THREE.Raycaster();
    raycaster.setFromCamera(mouse, camera);

    // z=0 の平面を定義
    const plane = new THREE.Plane(new THREE.Vector3(0, 0, 1), targetZ); // 法線ベクトル(0,0,1), 原点からの距離targetZ

    const intersectionPoint = new THREE.Vector3();
    raycaster.ray.intersectPlane(plane, intersectionPoint);

    return intersectionPoint;
}

// ワールド座標をスクリーン座標に変換するヘルパー関数
export function worldToScreenPosition(object: THREE.Object3D, camera: THREE.Camera, renderer: THREE.WebGLRenderer) {
    const vector = new THREE.Vector3();
    const canvas = renderer.domElement;

    // オブジェクトのワールド座標を取得
    object.updateWorldMatrix(true, false);
    vector.setFromMatrixPosition(object.matrixWorld);

    // カメラを通して正規化デバイス座標(NDC)に変換
    vector.project(camera);

    // NDCをスクリーン座標に変換
    vector.x = (vector.x + 1) / 2 * canvas.width;
    vector.y = -(vector.y - 1) / 2 * canvas.height; // Y軸は上下反転

    //console.error(":--------:", vector.x, vector.y);

    return { x: vector.x / window.devicePixelRatio, y: vector.y / window.devicePixelRatio };
}

